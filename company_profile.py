def get_company_profile(stock_id, stock_name="", industry="", theme="", theme_group=""):
    stock_id = str(stock_id).strip().zfill(4)[-4:]
    stock_name = str(stock_name or stock_id)
    industry = str(industry or "未分類")
    theme = str(theme or industry)
    theme_group = str(theme_group or theme)

    if stock_id in PROFILE_DB:
        p = PROFILE_DB[stock_id].copy()
    else:
        products, trend, rating = THEME_TEMPLATE.get(
            theme,
            (f"{industry}相關產品與服務", f"未來趨勢需搭配月營收、EPS、法人籌碼與產業景氣交叉確認", "★★★☆☆"),
        )
        p = {
            "主要產品": products,
            "主要客戶": "依公司年報與法說會揭露為準；目前使用產業別進行初步推估",
            "產業地位": f"屬於{theme_group}／{theme}族群，需進一步觀察市占率與獲利品質",
            "未來趨勢": trend,
            "趨勢評級": rating,
        }

    p.update({
        "股票代號": stock_id,
        "股票名稱": stock_name,
        "官方產業": industry,
        "主流族群": theme,
        "族群大類": theme_group,
    })
    return p
